    private void btnSalvarTabelaEditadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarTabelaEditadaActionPerformed
        String lojaOrigem = String.valueOf(comboLojaOrigem.getSelectedItem());
        String sistema = String.valueOf(comboSistema.getSelectedItem());
        System.out.println("Salvando editados: " + sistema + " - " + lojaOrigem);
        switch (opcao) {
            case 1: {
                if (getItensMgv() != null) {
                    try {
                        rep.atulizaMgv(getItensMgv(), sistema, lojaOrigem);
                    } catch (Exception ex) {
                        System.out.println(ex.getMessage());
                        JOptionPane.showMessageDialog(this, "Houve um erro inesperado!\n\n"
                                + "Favor confira o terminal e entre em contato com a Migração.\n\n");
                        Exceptions.printStackTrace(ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Precisa carregar o arquivo ItensMGV.");
                }
                break;
            }
            case 2: {
                if (getItensMgv() != null) {
                    try {
                        rep.atulizaInfnutri(getInfNutri(), sistema, lojaOrigem);
                    } catch (Exception ex) {
                        System.out.println(ex.getMessage());
                        JOptionPane.showMessageDialog(this, "Houve um erro inesperado!\n\n"
                                + "Favor confira o terminal e entre em contato com a Migração.\n\n");
                        Exceptions.printStackTrace(ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Precisa carregar o arquivo ItensMGV.");
                }
                break;
            }
            case 3: {
                if (getListaTxtInfo() != null) {
                    try {
                        rep.atulizaTxtinfo(getListaTxtInfo(), sistema, lojaOrigem);
                    } catch (Exception ex) {
                        System.out.println(ex.getMessage());
                        JOptionPane.showMessageDialog(this, "Houve um erro inesperado!\n\n"
                                + "Favor confira o terminal e entre em contato com a Migração.\n\n");
                        Exceptions.printStackTrace(ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Precisa carregar o arquivo Txtinfo.");
                }
            }
            default: {
                break;
            }
        }
    }//GEN-LAST:event_btnSalvarTabelaEditadaActionPerformed

