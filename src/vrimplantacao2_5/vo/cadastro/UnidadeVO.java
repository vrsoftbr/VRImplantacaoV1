package vrimplantacao2_5.vo.cadastro;

/**
 *
 * @author Desenvolvimento
 */
public class UnidadeVO {

    private int id;
    private String nome;
    private int	idMunicipio;
    private String descricaoMunicipio;
    private int idEstado;
    private String descricaoEstado;
    
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the idMunicipio
     */
    public int getIdMunicipio() {
        return idMunicipio;
    }

    /**
     * @param idMunicipio the idMunicipio to set
     */
    public void setIdMunicipio(int idMunicipio) {
        this.idMunicipio = idMunicipio;
    }

    /**
     * @return the descricaoMunicipio
     */
    public String getDescricaoMunicipio() {
        return descricaoMunicipio;
    }

    /**
     * @param descricaoMunicipio the descricaoMunicipio to set
     */
    public void setDescricaoMunicipio(String descricaoMunicipio) {
        this.descricaoMunicipio = descricaoMunicipio;
    }

    /**
     * @return the idEstado
     */
    public int getIdEstado() {
        return idEstado;
    }

    /**
     * @param idEstado the idEstado to set
     */
    public void setIdEstado(int idEstado) {
        this.idEstado = idEstado;
    }

    /**
     * @return the descricaoEstado
     */
    public String getDescricaoEstado() {
        return descricaoEstado;
    }

    /**
     * @param descricaoEstado the descricaoEstado to set
     */
    public void setDescricaoEstado(String descricaoEstado) {
        this.descricaoEstado = descricaoEstado;
    }
    
}
