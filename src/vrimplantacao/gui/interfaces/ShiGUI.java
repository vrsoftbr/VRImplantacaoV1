    public void validarDadosAcessoFirebird() throws Exception {
        if (txtHost.getText().isEmpty()) {
            throw new VRException("Favor informar host do banco de dados Firebird!");
        }
        if (txtBancoDados.getArquivo().isEmpty()) {
            throw new VRException("Favor informar nome do banco de dados Firebird!");
        }

        if (txtSenha.getText().isEmpty()) {
            throw new VRException("Favor informar a senha do banco de dados Firebird!");
        }

        if (txtPorta.getText().isEmpty()) {
            throw new VRException("Favor informar a porta do banco de dados Firebird!");
        }
        if (txtUsuario.getText().isEmpty()) {
            throw new VRException("Favor informar o usuário do banco de dados Firebird!");
        }

        connFB.abrirConexao(txtHost.getText(), txtPorta.getInt(), 
                txtBancoDados.getArquivo(), txtUsuario.getText(), txtSenha.getText());

        vLojaDestino = new LojaDAO().carregar();

        carregarLojaOrigem();
        carregarLojaDestino();
    }

    public void carregarLojaOrigem() throws Exception {
        cmbLojaOrigem.removeAllItems();
        for (ItemComboVO item : importacaoShiDAO.carregarLojasCliente()) {
            cmbLojaOrigem.addItem(item);
        }
    }
    
    public void carregarLojaDestino() throws Exception {
        cmbLojaDestino.removeAllItems();
        for (LojaVO oLoja : vLojaDestino) {
            cmbLojaDestino.addItem(new ItemComboVO(oLoja.id, oLoja.descricao));
        }

    }

    public void importarProdutosBalanca() {
        Thread thread = new Thread() {
            
            int opcao;
            
            @Override
            public void run() {
                
                try {
                    
                    ProgressBar.show();
                    ProgressBar.setCancel(false);
                    
                    if ((!txtArquivoBalanca.getArquivo().isEmpty()) &&
                            (rdbCadTxt.isSelected()) ||
                            (rdbTxtItens.isSelected()) ||
                            (rdbItensMgv.isSelected())) {
                        
                        if (rdbCadTxt.isSelected()) {
                            opcao = 1;
                        } else if (rdbTxtItens.isSelected()) {
                            opcao = 2;
                        } else if (rdbItensMgv.isSelected()) {
                            opcao = 3;
                        }
                        
                        importacaoShiDAO.importarProdutoBalanca(txtArquivoBalanca.getArquivo(), opcao);
                    }
                    
                    
                    ProgressBar.dispose();

                    Util.exibirMensagem("Importação Milenio realizada com sucesso!", getTitle());
                    
                } catch(Exception ex) {
                    
                    ProgressBar.dispose();
                    Util.exibirMensagemErro(ex, getTitle());
                }
            }
            
            
        };
                
        thread.start();
    }
    
    public void importarTabelas() throws Exception {
        Thread thread = new Thread() {
            int idLojaVR, idLojaCliente;
            @Override
            public void run() {
                try {
                    ProgressBar.show();
                    ProgressBar.setCancel(true);
                    
                    idLojaVR = cmbLojaDestino.getSelectedIndex()+1;                    
                    idLojaCliente = ((ItemComboVO) cmbLojaOrigem.getSelectedItem()).id;
                    
                    if (chkFamiliaProduto.isSelected()) {                        
                        importacaoShiDAO.importarFamiliaProduto();
                    }

                    if (chkMercadologico.isSelected()) {
                        importacaoShiDAO.importarMercadologico();
                    }

                    if (chkProduto.isSelected()) {
                        importacaoShiDAO.importarProdutoShi(idLojaVR);
                    }

                    if (chkCustoProduto.isSelected()) {
                        importacaoShiDAO.importarCustoProduto(idLojaVR, idLojaCliente);
                    }                    

                    if (chkPrecoProduto.isSelected()) {
                        importacaoShiDAO.importarPrecoProduto(idLojaVR, idLojaCliente);
                    }                                        

                    if (chkEstoqueProduto.isSelected()) {
                        importacaoShiDAO.importarEstoqueProduto(idLojaVR, idLojaCliente);
                    }                      

                    if (chkFornecedor.isSelected()) {
                        importacaoShiDAO.importarFornecedor();
                    } 

                    if (chkProdutoFornecedor.isSelected()) {
                        importacaoShiDAO.importarProdutoFornecedor();
                    }

                    if (chkClientePreferencial.isSelected()) {
                        importacaoShiDAO.importarClientePreferencial(idLojaVR, idLojaCliente);
                    } 

                    if (chkCreditoRotativo.isSelected()) {
                        importacaoShiDAO.importarReceberClienteShi(idLojaVR, idLojaCliente);
                    }                    

                    if (chkChequeReceber.isSelected()) {
                        importacaoShiDAO.importarChequeReceber(idLojaVR, idLojaCliente);
                    }

                    if (chkClienteDuplicado.isSelected()) {
                        importacaoShiDAO.corrigirClienteDuplicado();
                    }

                    if (chkCodigobarras.isSelected()) {
                        importacaoShiDAO.importarCodigoBarra();
                    }

                    if (chkICMS.isSelected()) {
                        importacaoShiDAO.importarICMS();
                    }                    

                    if (chkCodigoBarrasDuplicados.isSelected()) {
                        importacaoShiDAO.importarCodigoBarraEmBranco();
                    }

                    if (chkChequeQuitarPagos.isSelected()) {
                        importacaoShiDAO.importarQuitacaoDeChequesDevolvidos(idLojaVR, idLojaCliente);
                    }

                    ProgressBar.dispose();
                    Util.exibirMensagem("Importação SHI realizada com sucesso!", getTitle());
                } catch (Exception ex) {
                    try {                    
                        connFB.close();
                    } catch (Exception ex1) {
                        Exceptions.printStackTrace(ex1);
                    }
                    ProgressBar.dispose();
                    Util.exibirMensagemErro(ex, getTitle());
                }
            }
        };

        thread.start();
    }

