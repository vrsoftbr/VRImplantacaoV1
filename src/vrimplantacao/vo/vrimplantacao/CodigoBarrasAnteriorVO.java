package vrimplantacao.vo.vrimplantacao;

public class CodigoBarrasAnteriorVO {
    private double codigoAnterior;
    private int codigoAtual;
    private long codigoBarras;
    private int idLoja;

    /**
     * @return the codigoAnterior
     */
    public double getCodigoAnterior() {
        return codigoAnterior;
    }

    /**
     * @param codigoAnterior the codigoAnterior to set
     */
    public void setCodigoAnterior(double codigoAnterior) {
        this.codigoAnterior = codigoAnterior;
    }

    /**
     * @return the codigoAtual
     */
    public int getCodigoAtual() {
        return codigoAtual;
    }

    /**
     * @param codigoAtual the codigoAtual to set
     */
    public void setCodigoAtual(int codigoAtual) {
        this.codigoAtual = codigoAtual;
    }

    /**
     * @return the codigoBarras
     */
    public long getCodigoBarras() {
        return codigoBarras;
    }

    /**
     * @param codigoBarras the codigoBarras to set
     */
    public void setCodigoBarras(long codigoBarras) {
        this.codigoBarras = codigoBarras;
    }

    /**
     * @return the idLoja
     */
    public int getIdLoja() {
        return idLoja;
    }

    /**
     * @param idLoja the idLoja to set
     */
    public void setIdLoja(int idLoja) {
        this.idLoja = idLoja;
    }
}
